package practica_01;

// Ejercicio 01: Escribir un programa que declare variables para los tipos int,
// double, char y boolean, luego asigne un valor, a cada una, correspondiente al tipo 
// que tiene la variable, e imprima por pantalla cada una de las variables. 

public class Practica01_Ejercicio01 {
	public static void main(String[] args) {
		
		// Declaración de variables y asignación de valores
		int entero = 22;
		double real = 22.22;
		char unicode = '2';
		boolean bandera = false;
		
		// Impresión por pantalla
		System.out.println(entero);
		System.out.println(real);
		System.out.println(unicode);
		System.out.println(bandera);
		
		}
}
